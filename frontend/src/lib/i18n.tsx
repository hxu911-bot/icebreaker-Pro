const translations = { zh, en };

const LanguageContext = createContext<{
  lang: Lang;
  setLang: (l: Lang) => void;
  t: typeof zh;
}>({ lang: 'zh', setLang: () => {}, t: zh });

export function LanguageProvider({ children }: { children: React.ReactNode }) {
  const [lang, setLangState] = useState<Lang>(() => {
    const stored = localStorage.getItem('lang');
    return (stored === 'en' || stored === 'zh') ? stored : 'zh';
  });

  const setLang = useCallback((l: Lang) => {
    localStorage.setItem('lang', l);
    setLangState(l);
  }, []);

  return (
    <LanguageContext.Provider value={{ lang, setLang, t: translations[lang] }}>
      {children}
    </LanguageContext.Provider>
  );
}

export function useT() {
  return useContext(LanguageContext);
}

export function LangToggle() {
  const { lang, setLang } = useT();
  return (
    <div className="flex items-center gap-0 text-xs font-medium border border-gray-200 rounded-full overflow-hidden">
      <button
        onClick={() => setLang('zh')}
        className={`px-2.5 py-1 transition-colors ${lang === 'zh' ? 'bg-sky-100 text-sky-700' : 'text-gray-400 hover:text-gray-600'}`}
      >
        CN
      </button>
      <button
        onClick={() => setLang('en')}
        className={`px-2.5 py-1 transition-colors ${lang === 'en' ? 'bg-sky-100 text-sky-700' : 'text-gray-400 hover:text-gray-600'}`}
      >
        EN
      </button>
    </div>
  );
}
